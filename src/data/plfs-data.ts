// Helper functions
export function getStateByCode(code: string): StateEmploymentData | undefined {
	return stateData.find((s) => s.stateCode === code);
}

export function getStatesByUnemploymentRate(ascending = true): StateEmploymentData[] {
	return [...stateData].sort((a, b) =>
		ascending ? a.unemploymentRate - b.unemploymentRate : b.unemploymentRate - a.unemploymentRate,
	);
}

export function getTopUnemployedStates(n = 5): StateEmploymentData[] {
	return getStatesByUnemploymentRate(false).slice(0, n);
}

export function getLowestUnemployedStates(n = 5): StateEmploymentData[] {
	return getStatesByUnemploymentRate(true).slice(0, n);
}

export function getYouthUnemploymentData(): AgeGroupData[] {
	return ageGroupData.filter((d) => ["15-19", "20-24", "25-29"].includes(d.ageGroup));
}

export function getUnemploymentColor(rate: number): string {
	if (rate <= 2.5) return "#22c55e"; // Green - very low
	if (rate <= 4.0) return "#84cc16"; // Lime - low
	if (rate <= 5.5) return "#eab308"; // Yellow - moderate
	if (rate <= 7.0) return "#f97316"; // Orange - high
	return "#ef4444"; // Red - very high
}

export function getLfprColor(rate: number): string {
	if (rate >= 60) return "#22c55e"; // Green - very high participation
	if (rate >= 55) return "#84cc16"; // Lime - high
	if (rate >= 50) return "#eab308"; // Yellow - moderate
	if (rate >= 45) return "#f97316"; // Orange - low
	return "#ef4444"; // Red - very low
}
